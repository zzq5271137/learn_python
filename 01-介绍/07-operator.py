"""
运算符
"""

# 加号运算符: +
print("加号运算符: +")
a = 1
b = 2
c = a + b  # 数值相加
print(c)
print(type(c))
d = "1"
e = "2"
f = d + e  # 字符串拼接
print(f)
print(type(f))

print("#######################")

# 减号运算符: -
print("减号运算符: -")
g = 2
h = 1
i = g - h
print(i)
print(type(i))

print("#######################")

# 乘号运算符: *
print("乘号运算符: *")
j = 2
k = 3.2
l = j * k
print(l)
print(type(l))
m = "super8"
n = 3 * m
print(n)
print(type(n))

print("#######################")

# 除号运算符: /
print("除号运算符: /")
o = 11
p = 2
q = o / p
print(q)
print(type(q))

print("#######################")

# 整除运算符: //
print("整除运算符: //")
r = o // p
print(r)
print(type(r))

print("#######################")

# 取余运算符: %
print("取余运算符: %")
s = o % p
print(s)
print(type(s))

print("#######################")

# 幂运算符: **
print("幂运算符: **")
t = 3
u = 2
v = t ** u  # 3的2次方
print(v)
print(type(v))
v = u ** t  # 2的3次方
print(v)
print(type(v))

